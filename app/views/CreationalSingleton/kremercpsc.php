<H2>Singleton</H2>
<img src="/public/images/singleton.gif"><br>
Ensure a class only has one instance, and provide a global point of access to it.
<h3>Applicability</h3>
  <UL>
  <LI>Need exactly one instance of a class and a well-known access point
  <LI>Need to have the sole instance extensible by subclassing
  </UL>
<h3>Consequences</h3>
  <UL>
  <LI>Need controlled access
  <LI>Reduce name space
  <LI>Permits refinement of operations and representation
  <LI>More flexible than static class operators
  </UL>
