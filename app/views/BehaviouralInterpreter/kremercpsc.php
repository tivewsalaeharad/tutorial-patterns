<H2>Interpreter</H2>
<img src="/public/images/interpreter.gif"><p>
Given a language, define a representation for its grammar along with an interpreter
that uses the representation to interpret sentences in the language.
<h3>Applicability</h3>
  <UL>
  <LI>The grammar is simple
  <LI>Efficiency is not a critical concern
  </UL>
<h3>Consequences</h3>
  <UL>
  <LI>Easy to extend and change the grammar
  <LI>Implementing the grammar is easy
  <LI>Complex grammars are hard to maintain
  <LI>Easy to add new ways of interpreting the expressions
  </UL>
