<H2>Proxy</H2>
<img src="/public/images/proxy.gif"><p>
Provide a surrogate or placeholder for another object to control access to it.
<h3>Applicabilty</h3>
  <UL>
  <LI>Remote proxies can hide hide the fact that a real object is in another address space.
  <LI>Virtual proxies can create expensive objects on demand.
  <LI>Protection proxies can control access to an object.
  <LI>Smart references can preform additional action above a simple pointer.
  </UL>
