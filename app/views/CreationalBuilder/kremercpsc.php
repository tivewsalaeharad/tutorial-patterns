<H2>Builder</H2>
<img src="/public/images/builder.gif"><br>
Separate the construction of a complex object from its representation so
that the same construction process can ceatae different representations.
<h3>Applicability</h3>
  <UL>
  <LI>Need to isolate knowledge of the creation of a complex object from its parts
  <LI>Need to allow different implementations/interfaces of an object's parts
  </UL>
<h3>Consequences</h3>
  <UL>
  <LI>Lets you vary a product's internal representation
  <LI>Isolates code for construction and representation
  <LI>Finer control over the construction process
  </UL>
