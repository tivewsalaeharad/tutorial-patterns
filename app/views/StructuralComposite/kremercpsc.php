<H2>Composite</H2>
<img src="/public/images/composite.gif"><p>
Compose objects into tree structures to represent part-whole hierarchies.  Composite
lets clients treat individual objects and composition of objects uniformly.
<h3>Applicability</h3>
  <UL>
  <LI>Need to represent part-whole hierarchies
  <LI>Need to be able to ignore the difference between composites and individuals.
  </UL>
<h3>Consequences</h3>
  <UL>
  <LI>Makes the client simple
  <LI>Makes it easy to add new kinds of components
  <LI>Hard to restrict components of a composite
  </UL>
