<H2>Memento</H2>
<img src="/public/images/memento.gif"><p>
Without violating encapsulation, capture and externalize an object's internal state
so that the object can be restored to this state later.
<h3>Applicability</h3>
  <UL>
  <LI>Need to save a "snapshot"
  <LI>Need to save state without exposing the implementation of an object
  </UL>
<h3>Consequences</h3>
  <UL>
  <LI>Preserves encapsulation boundaries
  <LI>Simplifies Originator
  <LI>Can have high storage costs
  </UL>
